const failures = [];
const regionStats = {};

for (const [email, expectedFirst, expectedLast, region] of tests) {
  const expected = `${expectedFirst} ${expectedLast}`.trim();
  const result = extractNameFromEmail(email);

  // Initialize region stats
  if (!regionStats[region]) regionStats[region] = { passed: 0, failed: 0, total: 0 };
  regionStats[region].total++;

  const ok = result === expected;

  if (ok) {
    passed++;
    regionStats[region].passed++;
  } else {
    failed++;
    regionStats[region].failed++;
    failures.push({ email, expected, got: result, region });
  }
}

